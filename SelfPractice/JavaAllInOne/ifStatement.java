package JavaAllInOne;

public class ifStatement {
    public static void main(String[] args) {
        int score = 0;
        if (score == 0) {
            score += 50;
        } else if (score != 0) {
            score += 50;
        } else if (score != 0) {
            score += 50;
            System.out.println(score);
        }
    }
}
